/* check if a number is Armstrong
    (the sum of all the digits to the power of the length is the number itself) */
int isArmstrong(int num);

/* check if a number is Prime 
    divides only by 1 and himself*/
int isPrime(int num);


/* check if a number is Palindrome 
    can be read from both sides*/

int isPalindrome(int num);


/* check if a number is strong 
    the sum of the factorial of each digit is the numebr itself*/

int isStrong(int num);
